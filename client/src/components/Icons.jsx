// User Avatar Component with gradient based on role
export const UserAvatar = ({ name, role, size = 40 }) => {
  const getInitials = (name) => {
    if (!name) return '?';
    return name.charAt(0).toUpperCase();
  };

  const getRoleGradient = (role) => {
    const gradients = {
      'Admin': 'linear-gradient(135deg, #4f7cff 0%, #a855f7 100%)',
      'Doctor': 'linear-gradient(135deg, #22c55e 0%, #16a34a 100%)',
      'Nurse': 'linear-gradient(135deg, #06b6d4 0%, #0891b2 100%)',
      'Receptionist': 'linear-gradient(135deg, #f97316 0%, #ea580c 100%)',
      'Billing': 'linear-gradient(135deg, #eab308 0%, #ca8a04 100%)',
    };
    return gradients[role] || 'linear-gradient(135deg, #4f7cff 0%, #a855f7 100%)';
  };

  return (
    <div 
      style={{
        width: size,
        height: size,
        borderRadius: size * 0.25,
        background: getRoleGradient(role),
        display: 'flex',
        alignItems: 'center',
        justifyContent: 'center',
        fontSize: size * 0.45,
        fontWeight: 600,
        color: 'white',
        boxShadow: '0 2px 8px rgba(79, 124, 255, 0.25)',
        flexShrink: 0,
      }}
    >
      {getInitials(name)}
    </div>
  );
};

